let goUpBtn = document.querySelector("#go-up");
let circle = document.querySelector(".circle");
function dimensionCalculator () {
  let scrollTop = window.scrollY;
  let documentHeight = document.body.clientHeight;
  let windowHeight = window.innerHeight;
  let scrollPercent = scrollTop / (documentHeight - windowHeight);
  let scrollPercentRounded = Math.round(scrollPercent * 100);
  circle.style.background = `linear-gradient(to bottom,var(--main-color) ${scrollPercentRounded}%,transparent 0%)`;
}
function scrollTopHandler() {
  window.scrollTo(0, 0);
}

function positionChecker() {
  if (document.documentElement.scrollTop > 0) {
    circle.classList.add("visible");
  } else {
    circle.classList.remove("visible");
  }
}

if (window.innerWidth > 768){
  window.addEventListener("scroll",dimensionCalculator );
  goUpBtn.addEventListener("click",scrollTopHandler );
  document.addEventListener("scroll", positionChecker);
}
window.addEventListener('resize', ()=>{
  if (window.innerWidth > 768){
    window.addEventListener("scroll",dimensionCalculator );
    goUpBtn.addEventListener("click",scrollTopHandler );
    document.addEventListener("scroll", positionChecker);
  }else {
    circle.classList.remove("visible");
    window.removeEventListener("scroll",dimensionCalculator );
    goUpBtn.removeEventListener("click",scrollTopHandler );
    document.removeEventListener("scroll", positionChecker);
  }
})




/* scrollTop section ended */


